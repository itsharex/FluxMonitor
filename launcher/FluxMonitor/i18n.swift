import Foundation


class I18N: ObservableObject {
    static let shared = I18N()
    
    @Published var language: Language = .system {
        didSet {
            UserDefaults.standard.set(language.rawValue, forKey: "appLanguage")
            if language != .system {
                let code: String
                switch language {
                case .zh: code = "zh-Hans"
                case .zhTW: code = "zh-Hant"
                case .ja: code = "ja"
                case .ko: code = "ko"
                case .es: code = "es"
                case .de: code = "de"
                case .fr: code = "fr"
                case .it: code = "it"
                default: code = "en"
                }
                UserDefaults.standard.set([code], forKey: "AppleLanguages")
            } else {
                UserDefaults.standard.removeObject(forKey: "AppleLanguages")
            }
        }
    }
    
    var isZh: Bool {
        let currentLang = language == .system ? getSystemLang() : language
        return currentLang == .zh || currentLang == .zhTW
    }
    
    init() {
        if let saved = UserDefaults.standard.string(forKey: "appLanguage"),
           let lang = Language(rawValue: saved) {
            self.language = lang
        }
    }
    
    func t(_ key: String) -> String {
        let currentLang = language == .system ? getSystemLang() : language
        return translate(key, in: currentLang)
    }
    
    func tSystem(_ key: String) -> String {
        return translate(key, in: getSystemLang())
    }
    
    private func translate(_ key: String, in lang: Language) -> String {
        let dict: [String: String]
        switch lang {
        case .zh: dict = zhDict
        case .zhTW: dict = zhTWDict
        case .ja: dict = jaDict
        case .ko: dict = koDict
        case .es: dict = esDict
        case .de: dict = deDict
        case .fr: dict = frDict
        case .it: dict = itDict
        default: dict = enDict
        }
        return dict[key] ?? enDict[key] ?? key
    }
    
    private func getSystemLang() -> Language {
        var preferred = "en"
        if let appleLangs = CFPreferencesCopyAppValue("AppleLanguages" as CFString, kCFPreferencesAnyApplication) as? [String],
           let first = appleLangs.first {
            preferred = first
        } else {
            preferred = Locale.preferredLanguages.first ?? "en"
        }
        
        let lower = preferred.lowercased()
        if lower.hasPrefix("zh-hant") || lower.hasPrefix("zh-tw") || lower.hasPrefix("zh-hk") { return .zhTW }
        if lower.hasPrefix("zh-hans") || lower.hasPrefix("zh") { return .zh }
        if lower.hasPrefix("ja") { return .ja }
        if lower.hasPrefix("ko") { return .ko }
        if lower.hasPrefix("es") { return .es }
        if lower.hasPrefix("de") { return .de }
        if lower.hasPrefix("fr") { return .fr }
        if lower.hasPrefix("it") { return .it }
        return .en
    }
    
